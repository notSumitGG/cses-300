use std::io::Write;

fn main() {
    let mut token = Scanner::new(std::io::stdin().lock());
    let mut out = std::io::BufWriter::new(std::io::stdout().lock());
    let mut n = token.next::<u32>();
    let mut c = 0;
    while n > 4 {
        n /= 5;
        c += n;
    }
    writeln!(out, "{c}").unwrap();
}

